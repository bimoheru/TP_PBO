package PrakPBO5;

public class No7 {
     public static void main(String[] args){
        
        int i=0;
        System.out.println("i++");
        while (i<5){
            System.out.println("i++ : " + i);
            i++;
        }
        
        System.out.println("");
        i = 0;
        System.out.println("++i");
        while (i<5){
            ++i;
            System.out.println("++i : " + i);
        }
            
        
        
    }
}
